"""
Repetições
while (enquanto)
Executa uma ação enquanto uma condição for verdadeira
Loop infinito -> Quando um código não tem fim
"""
contador = 0

while contador <= 10:
    contador = contador + 1  #CASO O PRINT VENHA PARA CÁ, O CONTADOR IRA CONTAR ATÉ O 10 E RETORNAR,
    print(contador)

print('Acabou')